#!/usr/bin/env python
print("\n")

# integer
integerVariable = 3
type1 = type(integerVariable)
print(integerVariable)
print(type1)
print("\n")

# floating
floatVariable = 3.0
type2 = type(floatVariable)
print(floatVariable)
print(type2)
print("\n")

# string
stringVariable = "Hey, buddy- I'm a \"String\""
type3 = type(stringVariable)
print(stringVariable)
print(type3)
print("\n")

# Boolean
booleanVariableT = True
booleanVariableF = False
type4 = type(booleanVariableT)
type5 = type(booleanVariableF)
print(booleanVariableT)
print(booleanVariableF)
print(type4)
print(type5)
print("\n")
