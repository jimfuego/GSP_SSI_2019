#!/usr/bin/env python
print("\n")

# declare array
primes = [2, 3, 5, 7]

# print array elements
print("array elements")
for prime in primes:
    print(prime)
print("\n")

# print array elements (again)
print("array elements (again)")
for i in range(4):
    print(primes[i])
print("\n")

# Prints out the numbers 0,1,2,3,4
print("print 0-4")
for x in range(5):
    print(x)
print("\n")

# Prints out 3,4,5
print("print 3-5")
for x in range(3, 6):
    print(x)
print("\n")

# Prints out 3,5,7
print("print incremental range")
for x in range(3, 8, 2):
    print(x)
print("\n")

